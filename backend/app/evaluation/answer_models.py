"""Data models for answer-level evaluation."""

from datetime import datetime
from typing import Any, Literal

from pydantic import BaseModel, Field

AnswerScheme = Literal["closed_book", "full_paper", "vector_rag", "researchflow"]
GroundingStatus = Literal["grounded", "partially_grounded", "ungrounded", "refused"]
AnswerType = Literal[
    "factual",
    "numeric_table",
    "architecture",
    "training",
    "ablation",
    "comparison",
    "limitation",
]


class NumericValue(BaseModel):
    """A numeric value extracted from an answer for exact-match comparison."""

    raw: str
    value: float
    unit: str = ""
    is_percentage: bool = False


class GroundingCheck(BaseModel):
    """Citation and grounding verification for a single claim."""

    claim: str
    supported_by_cited: bool = False
    supported_by_any_evidence: bool = False
    cited_pages: list[int] = Field(default_factory=list)
    evidence_pages: list[int] = Field(default_factory=list)


class GeneratedAnswer(BaseModel):
    """An answer generated by one scheme for one test case."""

    case_id: str
    scheme: AnswerScheme
    answer_text: str
    input_tokens: int = 0
    output_tokens: int = 0
    latency_ms: int = 0
    api_cost_cny: float = 0.0
    tool_call_success: bool = True
    retrieval_status: str = "none"
    evidence_count: int = 0
    has_citations: bool = False
    grounding_status: GroundingStatus = "ungrounded"


class AnswerMetrics(BaseModel):
    """Answer-level metrics comparing generated answer to reference."""

    case_id: str
    scheme: AnswerScheme
    answer_type: AnswerType = "factual"

    # Numeric accuracy
    numeric_exact_match: bool = False
    numeric_tolerance_accuracy: float = 0.0
    numeric_values_in_answer: int = 0
    numeric_values_in_reference: int = 0

    # Semantic accuracy
    answer_correctness: float = 0.0  # 0-1, LLM Judge or keyword F1
    token_f1: float = 0.0
    keyword_coverage: float = 0.0

    # Faithfulness & hallucination
    faithfulness: float = 0.0  # 0-1
    hallucination_rate: float = 0.0
    unsupported_claim_rate: float = 0.0

    # Citation quality
    citation_precision: float = 0.0
    citation_recall: float = 0.0
    page_accuracy: float = 0.0

    # Refusal accuracy
    correct_refusal: bool = False
    should_refuse: bool = False

    # Grounding
    grounding_status: GroundingStatus = "ungrounded"

    # Token & cost
    input_tokens: int = 0
    output_tokens: int = 0
    full_context_tokens: int = 0
    rag_context_tokens: int = 0
    api_cost_cny: float = 0.0
    latency_ms: int = 0
    token_savings_ratio: float = 0.0

    # Judge metadata
    judge_reason: str = ""
    judge_method: Literal["deterministic", "llm_judge", "rule_based"] = "rule_based"


class AnswerCase(BaseModel):
    """A test case extended for answer-level evaluation."""

    case_id: str
    query: str
    paper_titles: list[str] = Field(default_factory=list)
    paper_ids: list[str] = Field(default_factory=list)
    relevant_pages: list[dict[str, Any]] = Field(default_factory=list)
    expected_answer: str | None = None
    answer_type: AnswerType = "factual"
    split: Literal["dev", "test"] = "test"
    label_status: str = "machine_generated"

    # Results from each scheme
    answers: dict[AnswerScheme, GeneratedAnswer] = Field(default_factory=dict)
    metrics: dict[AnswerScheme, AnswerMetrics] = Field(default_factory=dict)


class AnswerSchemeSummary(BaseModel):
    """Summary metrics for one answer scheme across all cases."""

    scheme: AnswerScheme
    case_count: int
    numeric_exact_match: float = 0.0
    numeric_tolerance_accuracy: float = 0.0
    answer_correctness: float = 0.0
    token_f1: float = 0.0
    faithfulness: float = 0.0
    hallucination_rate: float = 0.0
    unsupported_claim_rate: float = 0.0
    citation_precision: float = 0.0
    citation_recall: float = 0.0
    page_accuracy: float = 0.0
    correct_refusal_rate: float = 0.0
    grounding_rate: float = 0.0
    avg_input_tokens: float = 0.0
    avg_output_tokens: float = 0.0
    avg_latency_ms: float = 0.0
    avg_api_cost_cny: float = 0.0
    avg_token_savings: float = 0.0
    win_count: int = 0
    loss_count: int = 0
    tie_count: int = 0


class AnswerReport(BaseModel):
    """Complete answer-level evaluation report."""

    generated_at: datetime = Field(default_factory=lambda: datetime.now())
    dataset_path: str = ""
    schemes: list[AnswerScheme] = Field(default_factory=list)
    summaries: dict[str, AnswerSchemeSummary] = Field(default_factory=dict)
    by_answer_type: dict[str, dict[str, AnswerSchemeSummary]] = Field(default_factory=dict)
    by_paper: dict[str, dict[str, AnswerSchemeSummary]] = Field(default_factory=dict)
    bootstrap_ci: dict[str, list[float]] = Field(default_factory=dict)
    case_count: int = 0
    cases: list[AnswerCase] = Field(default_factory=list)
    failures: list[dict[str, Any]] = Field(default_factory=list)
